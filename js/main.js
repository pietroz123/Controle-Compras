$(document).ready(function() {

    
    /* =====================================================================================================================================================
    ================================================= FUNÇÕES DE FORMATAÇÃO DO FORM DE CADASTRO ============================================================
    ===================================================================================================================================================== */


    $("#form-cadastro").ready(function() {
        
        $("#input-cpf").mask('000.000.000-00');             /* Formata o CPF */
        $("#input-cep").mask('00000-000');                  /* Formata o CEP */
        $('#input-telefone').mask('(00) 00000-0000');       /* Formata o telefone */

    });


    /* =====================================================================================================================================================
    ================================================= APLICA CSS NA MENSAGEM DE ALERTA A PARTIR ============================================================
    ============================================================= DA LARGURA DA TELA =======================================================================
    ===================================================================================================================================================== */
    
    $('.mensagem-alerta').ready(function() {
        var w = $(window).width();
        var offset = $('.mensagem-alerta').offset().left;
        console.log(w);
        console.log(offset);
        
        $('.mensagem-alerta').css({
            'width': w,
            'margin-left': -offset + 15
        });
    });

});